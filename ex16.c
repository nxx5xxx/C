#include <stdio.h>
void main(){
	//c는 2차원배열도 배열수에 맞게 일렬로 나열해도 만들어진다 
	int arr01[2][3] = {90,60,80,70,85,75}; //이것도 2차원배열 
	int arr02[2][3] ={
		{75,95,80},
		{85,70,90}
	};
	
	int arr03[][4] ={
		{80,85},			//가변배열시 (2줄 4칸)
		{90,95,75,70},
		{65,70,85,80}					//남은 칸에는 알아서 0으로 채워준다
	};
	//배열의 순회
	printf("전산\tDB\tP/G\t총점\n");
	int i, j, tot=0;
	for(i=0;i<2;i++){
		for(j=0;j<3;j++){
			printf("%d\t",arr02[i][j]);
			tot+=arr02[i][j];
		}printf("%d\n",tot); tot=0;
	} 
}
